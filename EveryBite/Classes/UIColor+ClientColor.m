#import "UIColor+ClientColor.h"

@implementation UIColor (ClientColor)

//------------------------------------------------------------------------------
+ (UIColor *)clientBarColor
//------------------------------------------------------------------------------
{
    return [UIColor colorWithRed:0.99 green:0.99 blue:0.99 alpha:1.0];
}

//------------------------------------------------------------------------------
+ (UIColor *)clientActionColor
//------------------------------------------------------------------------------
{
    return [UIColor redColor];
}

@end
